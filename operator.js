console.log("operators in javascript")
a=15
b=4
console.log("a+b=",a+b)
console.log("a-b=",a-b)
console.log("a*b=",a*b)
console.log("a/b=",a/b)
console.log("a%b=",a%b)
console.log("a**b=",a**b)
console.log("a++=",a++)  // it will print 15
console.log(a)   // it will print 16

console.log("a++=",++a)   // in this case it will print 17
console.log(a)  // it will print 17

console.log("a--=",a--)
console.log(a)

console.log("--a=",--a)
console.log(a)
      